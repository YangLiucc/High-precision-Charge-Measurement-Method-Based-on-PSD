Q=2*10^(-14);%TM上的电荷,单位为C
CT=34.2*10^(-12);%TM和电极笼组成的总电容
m=1.96;%TM质量 kg
a=8.85*10^(-12);%真空介电常数
S=522*10^(-6);%有效电极面积
d0=4*10^(-3);%测试质量与电极间的距离
t=0:1:1*10^5;%时间
C0=1.15*10^(-12);%标称电容C0=a*S/d
%平动
u1=9;%四个电极施加的正弦电压幅值2,-2,1,-1
u2=9;%9,9,1,1
u3=1;
u4=1;
f1=1*2*pi;%平动 施加正弦电压的频率
f2=3*10^(-3)*2*pi;%转动 施加正弦电压的频率
U1=u1*sin(f1*t)+u3*sin(f2*t);
U2=-u1*sin(f1*t)+u4*cos(f2*t);
U3=u2*cos(f1*t)-u3*sin(f2*t);
U4=-u2*cos(f1*t)-u4*cos(f2*t);
a1=a*S/2*d0^(-2);%静电力/电压
UTM=Q/CT;%TM表面电势
F1=a1*(U1-UTM).^2;%四个电极和TM作用产生的静电力
F2=a1*(U2-UTM).^2;
F3=a1*(U3-UTM).^2;
F4=a1*(U4-UTM).^2;
F=F1-F4+F2-F3;%静电力合力
a2=F/m;%静电力引起的加速度
Umr=1;
Fx=-a*S*d0^(-2)/m*u3*Umr*UTM;
Qest=-Fx*d0^2*m/a/S/u3/Umr*CT;
a3=d0^2*m/a/S/u3/Umr*CT;%-负号